create or replace view public.admin_user_summary
with (security_invoker = true)
as
select
  p.id,
  p.email,
  p.display_name,
  p.is_admin,
  p.is_anonymous,
  p.created_at,
  count(distinct r.id) as receipt_count,
  count(distinct cpr.id) as price_report_count
from public.profiles p
left join public.receipts r
  on r.user_id = p.id
left join public.crowd_price_reports cpr
  on cpr.user_id = p.id
group by p.id;

create or replace function public.admin_delete_user_app_data(
  target_user_id uuid
)
returns void
language plpgsql
security definer
set search_path = public
as $$
begin
  if not public.is_admin() then
    raise exception 'Administrator access required';
  end if;

  delete from public.crowd_price_reports
  where user_id = target_user_id;

  delete from public.receipt_items
  where user_id = target_user_id;

  delete from public.receipts
  where user_id = target_user_id;

  update public.profiles
  set
    monthly_budget = 0,
    display_name = 'GrocerySaver User',
    updated_at = now()
  where id = target_user_id;
end;
$$;

