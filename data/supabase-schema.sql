-- -----------------------
-- Three-user / three-receipt rule
-- -----------------------

create or replace function public.apply_price_consensus(
  target_product_id bigint,
  target_store_id bigint,
  target_price_cents integer
)
returns void
language plpgsql
security definer
set search_path = public
as $$
declare
  distinct_user_count integer;
begin
  select count(distinct user_id)
  into distinct_user_count
  from public.crowd_price_reports
  where product_id = target_product_id
    and store_id = target_store_id
    and price_cents = target_price_cents;

  if distinct_user_count < 3 then
    return;
  end if;

  insert into public.prices (
    product_id,
    store_id,
    price_cents,
    source,
    crowd_verified,
    report_count,
    checked_date,
    updated_at
  )
  values (
    target_product_id,
    target_store_id,
    target_price_cents,
    'crowd_receipts',
    true,
    distinct_user_count,
    current_date,
    now()
  )
  on conflict (product_id, store_id)
  do update set
    price_cents = excluded.price_cents,
    source = excluded.source,
    crowd_verified = true,
    report_count = excluded.report_count,
    checked_date = excluded.checked_date,
    updated_at = now();
end;
$$;

create or replace function public.on_crowd_price_report()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  perform public.apply_price_consensus(
    new.product_id,
    new.store_id,
    new.price_cents
  );

  return new;
end;
$$;

drop trigger if exists crowd_price_report_consensus
on public.crowd_price_reports;

create trigger crowd_price_report_consensus
after insert on public.crowd_price_reports
for each row
execute function public.on_crowd_price_report();

