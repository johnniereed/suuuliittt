create or replace view public.admin_alias_candidates
with (security_invoker = true)
as
select
  ri.normalized_name,
  min(ri.raw_name) as sample_raw_name,
  ri.store_id,
  count(*) as occurrence_count,
  count(distinct ri.receipt_id) as receipt_count,
  count(distinct ri.user_id) as user_count,
  min(ri.created_at) as first_seen_at,
  max(ri.created_at) as last_seen_at
from public.receipt_items ri
where ri.matched_product_id is null
  and coalesce(ri.normalized_name, '') <> ''
group by ri.normalized_name, ri.store_id;

create or replace function public.apply_price_consensus(
  target_product_id bigint,
  target_store_id bigint,
  target_price_cents integer
)
returns void
language plpgsql
security definer
set search_path = public
as $$
declare
  distinct_user_count integer;
  distinct_receipt_count integer;
begin
  select
    count(distinct user_id),
    count(distinct receipt_id)
  into distinct_user_count, distinct_receipt_count
  from public.crowd_price_reports
  where product_id = target_product_id
    and store_id = target_store_id
    and price_cents = target_price_cents;

  if distinct_user_count < 3 or distinct_receipt_count < 3 then
    return;
  end if;

  insert into public.prices (
    product_id,
    store_id,
    price_cents,
    source,
    crowd_verified,
    report_count,
    checked_date,
    updated_at
  )
  values (
    target_product_id,
    target_store_id,
    target_price_cents,
    'crowd_receipts_3_users_3_receipts',
    true,
    least(distinct_user_count, distinct_receipt_count),
    current_date,
    now()
  )
  on conflict (product_id, store_id)
  do update set
    price_cents = excluded.price_cents,
    source = excluded.source,
    crowd_verified = true,
    report_count = excluded.report_count,
    checked_date = excluded.checked_date,
    updated_at = now();
end;
$$;

create or replace function public.admin_assign_receipt_alias(
  target_normalized_name text,
  target_product_id bigint,
  target_store_id bigint default null
)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  clean_alias text;
  updated_items integer := 0;
  inserted_reports integer := 0;
  candidate record;
begin
  if not public.is_admin() then
    raise exception 'Administrator access required';
  end if;

  clean_alias := lower(regexp_replace(trim(target_normalized_name), '[^a-z0-9 ]', ' ', 'g'));
  clean_alias := regexp_replace(clean_alias, '\s+', ' ', 'g');

  if clean_alias = '' then
    raise exception 'Alias cannot be empty';
  end if;

  insert into public.product_aliases(product_id, alias, normalized_alias)
  values (target_product_id, target_normalized_name, clean_alias)
  on conflict (product_id, normalized_alias) do nothing;

  update public.receipt_items
  set
    matched_product_id = target_product_id,
    matched_name = (select name from public.products where id = target_product_id),
    match_confidence = 1
  where matched_product_id is null
    and normalized_name = target_normalized_name
    and (target_store_id is null or store_id = target_store_id);

  get diagnostics updated_items = row_count;

  insert into public.crowd_price_reports(
    user_id,
    receipt_id,
    product_id,
    store_id,
    price_cents,
    reported_at
  )
  select distinct
    ri.user_id,
    ri.receipt_id,
    target_product_id,
    ri.store_id,
    ri.price_cents,
    ri.created_at
  from public.receipt_items ri
  where ri.matched_product_id = target_product_id
    and ri.normalized_name = target_normalized_name
    and ri.price_cents > 0
    and (target_store_id is null or ri.store_id = target_store_id)
  on conflict (user_id, receipt_id, product_id, store_id, price_cents)
  do nothing;

  get diagnostics inserted_reports = row_count;

  for candidate in
    select distinct store_id, price_cents
    from public.crowd_price_reports
    where product_id = target_product_id
      and (target_store_id is null or store_id = target_store_id)
  loop
    perform public.apply_price_consensus(
      target_product_id,
      candidate.store_id,
      candidate.price_cents
    );
  end loop;

  return jsonb_build_object(
    'alias', clean_alias,
    'updated_items', updated_items,
    'inserted_reports', inserted_reports
  );
end;
$$;

